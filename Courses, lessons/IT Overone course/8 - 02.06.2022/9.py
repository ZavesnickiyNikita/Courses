# Вывести статистику частности букв в кортеже

long_word = ('т', 'т', 'а', 'и', 'и', 'а', 'и', 'и', 'и', 'т', 'т', 'а',
             'и','и','и','и','и','т', 'и')
print(long_word.count('т'), 'Количество букв "т"')
print(long_word.count('а'), 'Количество букв "а"')
print(long_word.count('и'), 'Количество букв "и"')